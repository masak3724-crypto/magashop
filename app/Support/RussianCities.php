    public static function all(): array
    {
        return self::CITIES;
    }

    public static function isValid(string $city): bool
    {
        $normalized = mb_strtolower(trim($city));

        foreach (self::CITIES as $name) {
            if (mb_strtolower($name) === $normalized) {
                return true;
            }
        }

        return false;
    }

    public static function normalize(string $city): ?string
    {
        $normalized = mb_strtolower(trim($city));

        foreach (self::CITIES as $name) {
            if (mb_strtolower($name) === $normalized) {
                return $name;
            }
        }

        return null;
    }
